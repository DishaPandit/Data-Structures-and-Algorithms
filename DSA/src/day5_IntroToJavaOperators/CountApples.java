import java.util.Scanner;

public class CountApples {
	
public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();

        System.out.println((2*n + n-5) + " " + (m+5));
        
    }

}
